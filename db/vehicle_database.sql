CREATE TABLE IF NOT EXISTS Vehicle (
                    number VARCHAR(7) PRIMARY KEY,
                    model VARCHAR(20),
                    brand VARCHAR(20),
                    prodDate DATE);
CREATE TABLE IF NOT EXISTS ActivePlates (
                    number VARCHAR(7) PRIMARY KEY,
                    emissionDate DATE,
                    vehicleNumber VARCHAR(7),
                    FOREIGN KEY(vehicleNumber) REFERENCES Vehicle(number));
CREATE TABLE IF NOT EXISTS InactivePlates (
                    number VARCHAR(7) PRIMARY KEY,
                    emissionDate DATE,
                    vehicleNumber VARCHAR(7),
                    resDate DATE,
                    FOREIGN KEY(vehicleNumber) REFERENCES Vehicle(number));
CREATE TABLE IF NOT EXISTS Plates (
                    number VARCHAR(7) PRIMARY KEY,
                    emissionDate DATE,
                    vehicleNumber VARCHAR(7),
                    active BOOLEAN,
                    FOREIGN KEY(vehicleNumber) REFERENCES Vehicle(number));
CREATE TABLE IF NOT EXISTS Revisions (
                    id SERIAL PRIMARY KEY,
                    plateNumber VARCHAR(7),
                    revisionDate DATE,
                    outcome VARCHAR(10),
                    motivation TEXT,
                    FOREIGN KEY(plateNumber) REFERENCES Plates(number));
